    <h1 style="margin-top:0; font-size:22px;">KVKK Aydınlatma Metni</h1>
<h1>PUANDEKS Kişisel Verilerin Korunmasına İlişkin Aydınlatma Metni</h1>

<h2>1 - Giriş ve Kapsam</h2>
<p>PUANDEKS Bilişim ve Değerlendirme Hizmetleri Limited Şirketi (“PUANDEKS” veya “Şirket”), kişisel verilerinizin gizliliği ve güvenliğini temel ilke edinmiştir. 
Şirket; 6698 sayılı Kişisel Verilerin Korunması Kanunu (“KVKK”) ve ikincil mevzuata uygun olarak kişisel verileri işler, saklar ve gerekli hallerde aktarır. 
Bu Aydınlatma Metni, www.puandeks.com alan adlı internet sitesi ve mobil uygulamalar (birlikte “Platform”) üzerinden sunulan hizmetlerden yararlanan ilgili kişileri 
(kullanıcı/üye/ziyaretçi/işletme temsilcisi) bilgilendirmek amacıyla düzenlenmiştir.</p>
<p>PUANDEKS; Platform üyeliği, yorum/puan paylaşımı, işletmelerle etkileşim süreçleri, müşteri destek ve çağrı merkezi görüşmeleri ile 
şikâyet/başvuru süreçlerinde işlenen kişisel veriler bakımından veri sorumlusudur.</p>

<h2>2 - İşlenen Kişisel Veri Kategorileri</h2>
<p>Platform kullanım ve destek süreçleri kapsamında aşağıdaki veri kategorileri işlenebilir:</p>
<ul>
  <li><strong>Kimlik ve İletişim:</strong> ad, soyad, kullanıcı adı, profil bilgisi, e-posta, telefon, adres (varsa).</li>
  <li><strong>Hesap ve Profil:</strong> üyelik bilgileri, şifrelenmiş kimlik doğrulama verileri, tercih ve bildirim ayarları.</li>
  <li><strong>Müşteri İşlem/Talep:</strong> talep-şikâyet içerikleri, işlem ve etkileşim kayıtları, yanıt/çözüm bilgileri.</li>
  <li><strong>İçerik ve Topluluk:</strong> yorum, puan, görsel/video, tarih-saat ve içerik bağlamı.</li>
  <li><strong>İşlem Güvenliği/Log:</strong> IP, cihaz/erişim bilgileri, oturum kayıtları, uygulama hataları ve güvenlik logları.</li>
  <li><strong>Görsel İşitsel:</strong> çağrı merkezi ses kayıtları, destek yazışmaları ve gerekli hallerde ekran görüntüsü kanıtları.</li>
  <li><strong>Ödeme/Fatura (varsa):</strong> fatura/ödeme işlem bilgileri, muhasebe kayıtları.</li>
  <li><strong>Pazarlama/Ticari İletişim Tercihleri:</strong> izin/ret kayıtları, kampanya etkileşim bilgileri.</li>
  <li><strong>Çerez/SDK Verileri:</strong> çerez tanımlayıcıları, kullanım analitiği ve ölçüm verileri (ayrıntılar Çerez Politikası’nda).</li>
</ul>

<h2>3 - Kişisel Verilerin İşlenme Amaçları, Hukuki Sebepleri ve Toplama Yöntemleri</h2>
<p>Kişisel verileriniz; KVKK’nın 5 ve 6. maddelerindeki şartlar çerçevesinde aşağıdaki amaçlarla işlenir:</p>

<h3>3.1 Üyelik ve Hesap Yönetimi (KVKK m.5/2-c, 5/2-ç, 5/2-f)</h3>
<p>Üyelik sözleşmesinin kurulması/ifası, kimlik doğrulama, hesap oluşturma-güncelleme-kapama, bildirim ve tercihlerin yönetimi, Platform operasyonlarının yönetimi ve denetimi.</p>

<h3>3.2 Platform ve İçerik Süreçleri (m.5/2-c, 5/2-f)</h3>
<p>Yorum/puan ve topluluk içeriklerinin yayımlanması, sınıflandırılması, moderasyonu; kötüye kullanım ve ihlallerin önlenmesi; performans ve kullanılabilirliğin sağlanması.</p>

<h3>3.3 Müşteri Destek ve Çağrı Merkezi (m.5/2-c, 5/2-f)</h3>
<p>Taleplerin karşılanması, şikâyet yönetimi, kalite ve eğitim amaçlı ses kaydı alınması, iletişimlerin kayıt altına alınması.</p>

<h3>3.4 Bilgi Güvenliği, Risk, Denetim (m.5/2-c, 5/2-f)</h3>
<p>Güvenlik, loglama, siber olayların tespiti, denetim ve raporlama, dolandırıcılık ve kötüye kullanımın önlenmesi.</p>

<h3>3.5 Hukuki Yükümlülükler (m.5/2, 5/2-e)</h3>
<p>Mevzuat gerekliliklerinin yerine getirilmesi, yetkili kurum taleplerinin karşılanması, uyuşmazlık yönetimi ve hakların tesisi/kullanımı/korunması.</p>

<h3>3.6 Açık Rızaya Dayalı Faaliyetler (m.5/1)</h3>
<p>Pazarlama, kampanya ve kişiselleştirme; ticari elektronik iletiler (SMS/e-posta/arama). Verdiğiniz onayı dilediğiniz an geri alabilirsiniz. (İYS kayıt/tercih süreçleri mevzuata uygundur.)</p>

<h3>Toplama Yöntemleri</h3>
<p>Platform arayüzleri (web/mobil), çerez/SDK’lar, çağrı merkezi, e-posta/form ve destek kanalları, entegrasyon ve log sistemleri aracılığıyla otomatik/otomatik olmayan yollarla; gerektiğinde fiziki kanallar üzerinden veri toplanır.</p>

<h2>4 - Alıcı Grupları ve Yurt içi/Yurt Dışı Aktarımlar</h2>
<p>Kişisel verileriniz, veri minimizasyonu ilkesine uygun olarak, KVKK m.8 ve m.9 kapsamında ve gerekli teknik/idari önlemler alınmak suretiyle aşağıdaki alıcı gruplarıyla paylaşılabilir:</p>

<h3>Yurt İçi Alıcı Grupları</h3>
<ul>
  <li>Barındırma, güvenlik, çağrı merkezi, e-posta/SMS gönderimi, analitik ve ölçüm hizmeti sağlayıcıları,</li>
  <li>İş ortakları ve entegrasyon yapılan işletmeler (kullanıcı içeriklerinin ilgili işletme sitesindeki PUANDEKS bileşenlerinde gösterimi için gerekli ve ölçülü verilerle sınırlı olmak üzere),</li>
  <li>Hukuki/denetim danışmanları, bağımsız denetçiler, mali müşavirler,</li>
  <li>Düzenleyici ve denetleyici kurumlar ile yargı/idari merciler (mevzuattan doğan yükümlülükler çerçevesinde).</li>
</ul>

<h3>Yurt Dışı Alıcı Grupları</h3>
<ul>
  <li>Bulut barındırma, içerik dağıtım ağı (CDN), e-posta/ileti altyapısı, analitik ve hata/perf. izleme sağlayıcıları,</li>
  <li>Teknoloji tedarikçileri ve iş ortakları (Platform’un çalışması, güvenliği ve performansı için zorunlu olan hallerde).</li>
</ul>
<p>Yurt dışına aktarma; KVKK m.9 ve Kişisel Verilerin Yurt Dışına Aktarılmasına İlişkin Usul ve Esaslar Hakkında Yönetmelik uyarınca, “standart sözleşme” ile uygun güvencelerin sağlanması veya arızi hâllerde m.9/6 kapsamındaki istisnalar dayanak alınarak gerçekleştirilir.</p>

<h2>5 - Saklama ve İmha</h2>
<p>Mevzuatta belirlenmiş süreler varsa, ilgili kayıtlar en az bu süre kadar saklanır; olası hukuki talepler ve kayıtların tebliğ gecikmeleri gözetilerek bu sürelere 6–12 ay ek süre uygulanabilir. 
Mevzuatta süre öngörülmemişse, aramızdaki hukuki ilişkinin sona ermesinden itibaren kural olarak 10 yıl saklama süresi uygulanır.</p>
<p>Kişisel verilerin işlenmesini gerektiren sebeplerin tamamının ortadan kalkması veya saklama süresinin dolması halinde veriler, ilk periyodik imha döneminde ve en geç 6 ay içinde resmen silinir, yok edilir ya da anonim hâle getirilir. 
Geçerli bir sebebe dayalı silme/erteleme talepleri mevzuat sınırlarında en geç 30 gün içinde sonuçlandırılır. Detaylar “Veri Saklama ve İmha Politikası”nda düzenlenir.</p>

<h2>6 - Kişisel Verilerin Korunması</h2>
<ul>
  <li>İletişim ve işlem verileri aktarımında SSL/TLS ve benzeri kriptografik yöntemler,</li>
  <li>Erişim yetkisi ve kayıt izleme (log) kontrolleri, ayrıştırılmış ortamlar ve rol-tabanlı yetkilendirme,</li>
  <li>Güncel güvenlik yamaları, zararlı yazılım önleme ve tehdit istihbaratı,</li>
  <li>Periyodik zafiyet taraması/sızma testleri ve risk analizleri,</li>
  <li>Personel farkındalık eğitimleri ve gizlilik yükümlülükleri.</li>
</ul>

<h2>7 - Çerezler ve Benzeri Teknolojiler</h2>
<p>Platformda çerez ve benzeri teknolojiler kullanılmaktadır. Amaçlar, türler, saklama süreleri ve yönetim tercihlerine ilişkin ayrıntılar Çerez Politikası’nda yer almakta olup; tarayıcınızdan çerez tercihlerinizi yönetebilirsiniz.</p>

<h2>8 - İlgili Kişi Hakları (KVKK m.11)</h2>
<ul>
  <li>Kişisel verilerinizin işlenip işlenmediğini öğrenme ve bilgi talep etme,</li>
  <li>İşleme amaçlarını ve amacına uygun kullanımı öğrenme,</li>
  <li>Yurt içinde/yurt dışında aktarıldığı üçüncü kişileri bilme,</li>
  <li>Eksik/yanlış işlenmiş ise düzeltilmesini isteme,</li>
  <li>KVKK’daki şartlar kapsamında silinmesini/yok edilmesini isteme,</li>
  <li>Düzeltme/silme taleplerinin aktarıldığı üçüncü kişilere bildirilmesini talep etme,</li>
  <li>Münhasıran otomatik sistemler ile analiz sonucu aleyhe sonuca itiraz etme,</li>
  <li>Mevzuata aykırı işleme nedeniyle zararın giderilmesini talep etme.</li>
</ul>


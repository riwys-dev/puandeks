<p><strong>Not:</strong> Bu Politika genel bir çerçevedir. Veri işleme faaliyeti özelinde daha ayrıntılı bilgi içeren Aydınlatma Metni, Çerez Politikası ve Platform Kullanım Şartları ile birlikte okunmalıdır.</p>


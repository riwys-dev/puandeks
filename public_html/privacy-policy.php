<h2>2) Politikanın Kapsamı ve İlişkili Metinler</h2>
<ul>
  <li>Bu Politika, Platform üzerinde yürütülen tüm işlemler için geçerlidir.</li>
  <li>Çerezler ve benzeri teknolojiler bakımından Çerez Politikası esas alınır.</li>
  <li>Ticari iletişim tercihleri ve üyelik/kullanım şartları açısından Platform Kullanım Şartları ve Aydınlatma Metni önceliklidir.</li>
</ul>

<h2>3) İşlenen Kişisel Veri Kategorileri</h2>
<h3>3.1. Ziyaretçi Verileri (hesapsız kullanım)</h3>
<ul>
  <li>İşlem güvenliği ve teknik veriler (IP, cihaz/çerez tanımlayıcılar, tarayıcı bilgisi, tarih-saat damgası, oturum kayıtları, hata/günlük kayıtları).</li>
  <li>Çerez tercihleri (ayrıntı: Çerez Politikası).</li>
</ul>

<h3>3.2. Üyelik / Hesap Verileri (tüketici)</h3>
<ul>
  <li>Kimlik (ad-soyad, takma ad), iletişim (e-posta, telefon), hesap/parola (şifreler salted/hashed saklanır), profil görseli (varsa).</li>
  <li>Platform içi faaliyet verileri (yorum/puan, beğeni, şikâyet/itiraz kayıtları, bildirim tercihleri).</li>
  <li>İsteğe bağlı: Sipariş/hizmet teyidi için belgelendirme (fatura/fiş görseli, rezervasyon no vb.) yalnızca doğrulama amacıyla.</li>
</ul>

<h3>3.3. Üye İşletme / Yetkili Verileri</h3>
<ul>
  <li>Ticari unvan, MERSİS/vergisel bilgiler (işletmeye ait), yetkili/irtibat kişisinin kimlik ve iletişim bilgileri, fatura ve tahsilat kayıtları, entegrasyon ve widget kullanım verileri, sözleşme ve destek talepleri.</li>
</ul>

<h3>3.4. İçerik Verileri (Kullanıcı tarafından üretilen)</h3>
<ul>
  <li>Yorum, değerlendirme, puan, görsel/video (yüklenmişse), işletmeye verilen geri bildirimler ve işletmenin yanıtları.</li>
  <li><em>Uyarı:</em> İçerik alanlarında özel nitelikli kişisel veri (sağlık, siyasi düşünce vb.) paylaşmamanız gerekir.</li>
</ul>

<h3>3.5. Pazarlama / İletişim Tercihleri</h3>
<ul>
  <li>Ticari ileti onayı, abonelik/tercih geçmişi, kampanya etkileşimi (izinli).</li>
</ul>

<h3>3.6. Ödeme / Finans (Üye işletme)</h3>
<ul>
  <li>Fatura, tahsilat, paket/abonelik hareketleri. Kart verileri ödeme kuruluşu tarafından işlenir; Platform kart bilgilerini sistemlerinde tutmaz.</li>
</ul>

<h2>4) Toplama Yöntemleri</h2>
<ul>
  <li>Doğrudan sizden (kayıt formları, panel, destek, itiraz/şikâyet başvuruları, sözleşme süreçleri),</li>
  <li>Otomatik yollarla (çerezler, SDK’lar, loglar),</li>
  <li>Üçüncü kişilerden (ödeme kuruluşu, barındırma/iletişim sağlayıcıları, güvenlik/antispam araçları) ve gerektiğinde resmî mercilerden.</li>
</ul>

<h2>5) İşleme Amaçları</h2>
<ol>
  <li><strong>Hizmet sunumu ve sözleşmenin ifası:</strong> Üyelik oluşturma/kimlik doğrulama, yorum/puan yayınlama, işletme paneli, entegrasyon ve widget’ların çalışması, bakım/destek.</li>
  <li><strong>İçerik doğrulama ve kötüye kullanımın önlenmesi:</strong> Sahte yorum tespiti, tehdit/şantaj/nefret söylemi ve mevzuata aykırı içeriklerin incelenmesi; notice-and-takedown süreçleri; güvenlik/uygulama logları.</li>
  <li><strong>Şeffaflık ve etkileşim:</strong> İşletmelerin kendileriyle ilgili yapılan yorumlara cevap verebilmesi ve gerekli hallerde işlem/iş ilişkisinin doğrulanması.</li>
  <li><strong>Sıralama ve görünürlük:</strong> Puan/kalite, etkileşim ve uygunluk kriterlerine dayalı listeleme/algoritmik sıralama.</li>
  <li><strong>İletişim:</strong> Bildirimler, değişiklik duyuruları, anket/memnuniyet iletileri; ticari ileti yalnızca onay ile.</li>
  <li><strong>Güvenlik ve kesintisiz hizmet:</strong> Dolandırıcılık ve siber güvenlik tedbirleri, yedekleme, iz kayıtlarının tutulması.</li>
  <li><strong>Hukuki yükümlülükler:</strong> Taleplere yanıt, uyuşmazlık yönetimi, resmî makam taleplerinin yerine getirilmesi, mevzuat saklama yükümlülükleri.</li>
  <li><strong>İyileştirme ve raporlama:</strong> Ürün/hizmet geliştirme, hata ayıklama, ölçümleme/analitik.</li>
</ol>

<h2>6) Hukuki Sebepler (KVKK m.5 ve m.6)</h2>
<ul>
  <li>Sözleşmenin kurulması/ifası (m.5/2-c): Üyelik, panel, yorum ve entegrasyon hizmetleri.</li>
  <li>Hukuki yükümlülük (m.5/2-ç): Saklama, resmî taleplere yanıt, uyuşmazlık süreçleri.</li>
  <li>Hakkın tesisi/kullanılması/korunması (m.5/2-e): İtiraz/şikâyet, içerik kaldırma prosedürleri, alacak/tazminat hakları.</li>
  <li>Meşru menfaat (m.5/2-f): Güvenlik, sahte yorum ve suistimalin önlenmesi, hizmetlerin geliştirilmesi, algoritmik sıralama.</li>
  <li>Açık rıza (m.5/1): Ticari ileti, analitik/kişiselleştirme çerezleri, zorunlu olmayan veri işleme faaliyetleri.</li>
  <li>Özel nitelikli veriler (m.6): Platform bu verileri talep etmez; kullanıcı tarafından içerikte paylaşılması önerilmez. Zorunlu hale gelirse açık rıza ve hukuka uygunluk şartları aranır.</li>
</ul>

<h2>7) Alıcı Grupları ve Aktarımlar</h2>
<p>Kişisel verileriniz; amaçla sınırlı olmak üzere teknoloji/altyapı hizmet sağlayıcıları (barındırma, CDN, e-posta/ileti, çağrı merkezi, güvenlik/antispam, loglama, analitik, ödeme kuruluşu), hukuk/mali müşavirlik ve denetim hizmetleri, yetkili kamu kurumları/mahkemeler, grup şirketleri/iş ortakları (gerekliyse ve ölçülü şekilde) ve <strong>Üye İşletme</strong> ile (yalnızca ilgili yorum/olay bağlamında doğrulama ve yanıt verme amaçlarıyla; pazarlama/ısrarlı ileti/taciz yasaktır) paylaşılabilir.</p>
<p><strong>Yurt dışına aktarım:</strong> Barındırma, CDN, e-posta, analitik ya da benzeri hizmetler nedeniyle yurt dışına aktarım söz konusu olabilir. Bu hallerde KVKK m.9 uyarınca uygun mekanizmalar (yeterlilik, taahhütname, açık rıza vb.) uygulanır. Ayrıntılar Aydınlatma Metni ve Çerez Politikası’nda yer alır.</p>

<h2>8) Çerezler ve Benzeri Teknolojiler</h2>
<ul>
  <li>Zorunlu çerezler her zaman çalışır.</li>
  <li>Analitik/kişiselleştirme/reklam çerezleri için açık rıza alınır ve tercihler <em>[Çerez Ayarları]</em> üzerinden yönetilebilir.</li>
  <li>Ayrıntı için Çerez Politikası’na bakınız.</li>
</ul>

<h2>9) Kamuya Açık İçerikler ve Üçüncü Kişi Verileri</h2>
<ul>
  <li>Yorum ve değerlendirmeleriniz kamuya açık olarak gösterilebilir. Bu alanlarda başkalarına ait kişisel verileri paylaşmamanız gerekir.</li>
  <li>Üçüncü kişiye ait veri paylaşırsanız, KVKK kapsamındaki bilgilendirme ve onay yükümlülüklerinin yerine getirilmesinden siz sorumlusunuz.</li>
</ul>

<h2>10) Çocukların Gizliliği</h2>
<p>Platform 18 yaş altı kişiler için tasarlanmamıştır. 18 yaş altındaysanız lütfen Platformu veli/vasi izni olmadan kullanmayınız.</p>

<h2>11) Saklama Süreleri</h2>
<p>Veriler, amaç için gerekli süre boyunca ve ilgili mevzuatın öngördüğü zamanaşımı/saklama süreleri dikkate alınarak tutulur.</p>
<ul>
  <li>Üyelik ve işlem kayıtları: Sözleşme ilişkisi + zamanaşımı.</li>
  <li>Loglar ve güvenlik kayıtları: 5651 ve ilgili mevzuat süreleri.</li>
  <li>Yorum/içerik: Kullanıcı silene kadar; hukuki uyuşmazlık durumunda sonuçlanana dek.</li>
  <li>Fatura/tahsilat: Vergi/TTK saklama süreleri.</li>
</ul>
<p>Süre dolduğunda veri silinir, yok edilir veya anonimleştirilir.</p>

<h2>12) Profil Oluşturma ve Sıralama (Algoritma)</h2>
<ul>
  <li>Görünürlük; puan/kalite, içerik uygunluğu, güncellik, etkileşim ve ihlal oranları gibi parametreleri dikkate alan bir sıralama algoritması ile belirlenebilir.</li>
  <li>Bu faaliyet tamamen otomatik bir karar ile hukuki/benzer etkiler doğuracak nitelikte değildir.</li>
  <li>İtiraz/açıklama talebi ile parametreler hakkında genel bilgi isteyebilirsiniz (bkz. Başvuru Yöntemleri).</li>
</ul>

<h2>13) Veri Güvenliği</h2>
<p>Kişisel veriler; şifreleme, erişim kontrolü, ağ güvenliği, yedekleme, loglama ve fiziksel/idari tedbirlerle korunur. Kart verileri Platform sistemlerinde saklanmaz; ödeme işlemleri lisanslı ödeme kuruluşu üzerinden gerçekleşir. Her sistemde olduğu gibi %100 güvenlik garanti edilemez; şüpheli bir durum görürseniz bize derhâl bildirin.</p>

<h2>14) Üçüncü Taraf Siteler ve Bağlantılar</h2>
<p>Platform, üçüncü taraf sitelere bağlantılar içerebilir. Bu sitelerin veri işleme faaliyetleri bu Politika kapsamı dışındadır; ilgili sitelerin politikalarını inceleyiniz.</p>


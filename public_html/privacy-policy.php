  <li>Eksik/yanlışsa düzeltilmesini isteme,</li>
  <li>KVKK m.7 kapsamında silinmesini/yok edilmesini isteme,</li>
  <li>Düzeltme/silme işlemlerinin aktarılan kişilere bildirilmesini isteme,</li>
  <li>Münhasıran otomatik sistemlerle analiz sonucu aleyhe bir sonuca itiraz,</li>
  <li>Kanuna aykırı işleme nedeniyle zararın giderilmesini talep.</li>
</ul>


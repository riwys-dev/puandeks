<h1>PUANDEKS BİREYSEL ÜYELİK VE PLATFORM KULLANIM SÖZLEŞMESİ</h1>

<h2>1. Taraflar ve Tanımlar</h2>
<p>İşbu Bireysel Üyelik ve Platform Kullanım Sözleşmesi (“<strong>Sözleşme</strong>”); MASLAK MAH. AHİ EVRAN CAD. OLIVE PLAZA NO:11, İÇ KAPI NO:2, SARIYER/İSTANBUL
adresinde mukim <strong>PUANDEKS BİLİŞİM VE DEĞERLENDİRME HİZMETLERİ LİMİTED ŞİRKETİ</strong> (“<strong>PUANDEKS</strong>”) ile, <strong>www.puandeks.com</strong>
alan adlı internet sitesi ve/veya mobil uygulamaya (“<strong>Platform</strong>”) üye olan ve Platform’u kullanan bireysel kullanıcı (“<strong>Üye</strong>”) arasında elektronik ortamda akdedilmiştir.</p>
<p>Bu Sözleşmede “<strong>Üye</strong>”, Platform’a bireysel hesapla kayıt olan gerçek kişiyi; “<strong>Hesabım/Profilim</strong>”, Üye’nin sadece kendi kullanıcı
adı/şifresiyle erişebildiği kişisel alanını; “<strong>İçerik</strong>”, Üye tarafından veya Platform’da görüntülenen metin, görsel, video, puan ve yorum gibi her
türlü paylaşımı; “<strong>Hizmet(ler)</strong>”, Üyelerin işletmeler hakkında deneyim paylaşabildiği, değerlendirme/puanlama yapabildiği ve bu içeriklerin
yayınlandığı tüm fonksiyonları ifade eder.</p>

<h2>2. Amaç ve Kapsam</h2>
<p>Platform; mal veya hizmet sağlayıcıları ile kullanıcıları şeffaf geri bildirim ortamında bir araya getirmeyi, deneyim ve değerlendirmelerin paylaşılmasıyla
hizmet kalitesinin artmasına katkı sağlamayı amaçlar. İşbu Sözleşme, Platform’u kullanacak Üyelerin uyması gereken koşullar ve tarafların hak-yükümlülüklerini
düzenler. Üye, Platform’u kullanmakla Sözleşme hükümlerine bağlı olmayı kabul eder.</p>

<h2>3. Üyelik Koşulları ve Hesap</h2>
<ul>
  <li>Üyelik için 18 yaşını doldurmak ve “Üye Ol” akışında talep edilen bilgileri doğru, eksiksiz ve güncel şekilde sağlamak zorunludur. Üye, verdiği bilgilerin doğruluğundan sorumludur.</li>
  <li>PUANDEKS, üyelik başvurusunu gerekçesiz reddedebilir, ek bilgi/belge isteyebilir; dilediğinde üyeliği askıya alabilir veya sona erdirebilir.</li>
  <li>Üye; kullanıcı adı ve şifresinin güvenliğinden sorumludur. Hesap üzerinden yapılan işlemler Üye tarafından yapılmış sayılır. Bilgiler değiştiğinde derhal güncelleme yapmak Üye’nin yükümlülüğüdür.</li>
</ul>

<h2>4. Kullanım Kuralları ve Yasaklı Haller</h2>
<p>Üye, Platform’u mevzuata, dürüstlük kurallarına ve genel ahlaka uygun kullanmayı; üçüncü kişilerin haklarını ihlal etmemeyi kabul eder. Aşağıdaki fiiller yasaktır:</p>
<ul>
  <li>Platform’un tamamını/bir kısmını bozma, değiştirme, tersine mühendislik; zararlı yazılım yayma; orantısız yük oluşturarak hizmete müdahale; robot/crawler ile içerik kazıma.</li>
  <li>Yanıltıcı, hakaret içeren, iftira niteliğinde, kaba, müstehcen, nefret/ayrımcılık yayan, şiddeti teşvik eden, kişilik haklarını ihlal eden içerik gönderimi.</li>
  <li>Başkasını taklit etme, yanlış kimlik beyanı, sahte hesap; başkasının hesabını izinsiz kullanma.</li>
  <li>Promosyon, reklam, zincir mesaj, spam vb. istenmeyen içerik gönderimi.</li>
  <li>Üçüncü kişilerin kişisel verilerini hukuka aykırı şekilde paylaşma/işleme.</li>
</ul>
<p>PUANDEKS, ihlal durumunda içeriği kaldırabilir, görünürlüğü kısıtlayabilir, hesabı askıya alabilir/sonlandırabilir; gerekli hâllerde hukuki mercilere başvurabilir.</p>

<h2>5. İçerik Kuralları ve Moderasyon</h2>
<ul>
  <li>Üye, paylaştığı içeriklerin doğruluğundan ve hukuka uygunluğundan sorumludur.</li>
  <li>PUANDEKS; içerikleri yayımlamayı reddetme, sınıflandırma, imla/ifade sadeleştirme gibi düzenlemeler yapma, kısmen veya tamamen kaldırma yetkisine sahiptir; içeriklerin görünürlüğü ve sıralaması PUANDEKS’in takdirindedir.</li>
  <li>Dolandırıcılık şüphesi taşıyan, manipülatif, organize veya menfaat karşılığı üretildiği değerlendirilen içerik/etkileşimler iptal edilebilir veya kaldırılabilir.</li>
</ul>

<h2>6. Ticari Elektronik İletiler ve İletişim</h2>
<ul>
  <li>Üye, tercih etmesi hâlinde kampanya, duyuru, bülten ve bilgilendirme amaçlı arama/SMS/e-posta almayı kabul edebilir; tercihlerini dilediği an “Bildirim Tercihleri” alanından güncelleyebilir.</li>
  <li>Sipariş/işlem/bildirim amaçlı zorunlu iletiler tercih dışıdır.</li>
</ul>

<h2>7. Çerezler ve Dijital İzler</h2>
<p>Platform; deneyimi kişiselleştirmek, güvenliği sağlamak ve performansı ölçmek amacıyla çerezler ve benzeri teknolojiler kullanır. 
Üye, tarayıcı ayarlarından çerez tercihlerini yönetebilir; bazı kullanım verileri istatistik ve iyileştirme amacıyla anonimleştirilebilir.</p>


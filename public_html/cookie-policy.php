<h1>PUANDEKS Çerezlere İlişkin Aydınlatma Metni ve Çerez Politikası</h1>

<p><strong>Veri Sorumlusu:</strong> PUANDEKS (“Şirket”)<br>
<strong>E-posta:</strong> <a href="mailto:kurumsal@puandeks.com">kurumsal@puandeks.com</a><br>
<strong>KEP:</strong> kep</p>

<p>Bu metin, Şirket tarafından işletilen internet siteleri ve (varsa) mobil uygulamalarda (“Platform”)
kullanılan çerezler ve benzeri teknolojiler vasıtasıyla kişisel verilerin işlenmesine ilişkin usul ve
esasları, 6698 sayılı Kişisel Verilerin Korunması Kanunu (“KVKK”) m.10 kapsamındaki aydınlatma
yükümlülüğü doğrultusunda açıklar. Şirket kişisel verileri KVKK ve ilgili ikincil mevzuata uygun,
veri minimizasyonu ve amaçla bağlantılılık ilkeleri çerçevesinde işler.</p>

<p><em>Çerezlere ilişkin güncel ve ayrıntılı liste</em> (çerez adı, sağlayıcı, amaç, tür, saklama süresi ve varsa
yurt dışı aktarım bilgisi) Platform’un alt bölümünde yer alan <strong>Çerez Tercihleri</strong> panelinde anlık
olarak görüntülenir ve bu politikanın ayrılmaz parçasıdır.</p>

<h2>1) Tanımlar</h2>
<ul>
  <li><strong>Çerez (cookie):</strong> Bir internet sitesi veya uygulamayı kullandığınızda tarayıcınıza/cihazınıza yerleştirilen küçük metin dosyaları.</li>
  <li><strong>Benzeri teknolojiler:</strong> Piksel etiketleri, SDK, yerel depolama, beacon/web işaretçisi, reklam kimliği vb.</li>
  <li><strong>Birinci taraf çerez:</strong> Ziyaret edilen alan adı tarafından yerleştirilen çerez.</li>
  <li><strong>Üçüncü taraf çerez:</strong> Ziyaret edilen alan adından farklı bir alan adı tarafından yerleştirilen çerez.</li>
  <li><strong>Oturum çerezi:</strong> Tarayıcı kapanınca silinen çerez.</li>
  <li><strong>Kalıcı çerez:</strong> Belirli bir süre boyunca cihazda tutulan çerez.</li>
</ul>

<h2>2) Çerez Kategorileri ve Kullanım Amaçları</h2>
<h3>2.1. Kesinlikle Gerekli (Zorunlu) Çerezler</h3>
<p>Platform’un çalışması, oturum açma, form gönderimi, güvenlik (kötüye kullanım/sahtecilik önleme), yük dengeleme, ağ yönetimi ve gizlilik tercihlerinin 
hatırlanması için zorunludur. Pazarlama/hedefleme amacıyla kullanılmaz. <em>Hukuki sebep:</em> KVKK m.5/2(c) ve m.5/2(f).</p>

<h3>2.2. İşlevsellik (Tercih) Çerezleri</h3>
<p>Dil, bölge, erişilebilirlik ayarları ve kullanıcı tercihlerini hatırlayarak deneyimi kişiselleştirir. <em>Hukuki sebep:</em> KVKK m.5/1 (açık rıza).</p>

<h3>2.3. Analitik / Performans Çerezleri</h3>
<p>Ziyaret/ trafik ölçümü, sayfa performansı, hata tespiti, kapasite planlama ve özellik kullanım istatistikleri için kullanılır. <em>Hukuki sebep:</em> 
KVKK m.5/1 (açık rıza).</p>

<h3>2.4. Hedefleme / Pazarlama Çerezleri</h3>
<p>İlgi alanına dayalı içerik ve reklam gösterimi, yeniden hedefleme, kampanya etkinliği ölçümü ve profil çıkarımı için kullanılır; sosyal ağlar ve 
reklam ortaklarıyla çalışabilir. <em>Hukuki sebep:</em> KVKK m.5/1 (açık rıza).</p>

<p>Mobil uygulamalarda çerez yerine benzer amaçlarla SDK’lar kullanılabilir; yukarıdaki kategoriler, amaçlar ve hukuki sebepler aynen geçerlidir.</p>

<h2>3) Çerezler Yoluyla İşlenen Kişisel Veri Kategorileri ve Toplama Yöntemi</h2>
<p>Çerezler/benzeri teknolojiler vasıtasıyla tamamen otomatik yöntemlerle; cihaz ve tarayıcı bilgisi, IP/proxy, 
çerez kimliği, benzersiz tanımlayıcılar, onay/ret tercihleri, oturum bilgileri, zaman damgaları, referans URL, kampanya parametreleri, 
ziyaret edilen sayfalar, tıklama/kaydırma ve benzeri kullanıcı işlem kayıtları elde edilir.</p>

<h2>4) İşleme Amaçları ve Hukuki Sebep Eşleştirmesi</h2>
<ul>
  <li><strong>Hizmetin sunulması, güvenlik ve süreklilik (zorunlu):</strong> KVKK m.5/2(c), m.5/2(f).</li>
  <li><strong>Kullanıcı tercihlerinin hatırlanması (işlevsellik):</strong> KVKK m.5/1.</li>
  <li><strong>Ziyaret/performans ölçümü, hata &amp; kapasite yönetimi (analitik):</strong> KVKK m.5/1.</li>
  <li><strong>Kişiselleştirilmiş pazarlama ve kampanya ölçümü (hedefleme):</strong> KVKK m.5/1.</li>
  <li><strong>Mevzuat talepleri/uyuşmazlık yönetimi:</strong> KVKK m.5/2(ç), m.5/2(e), m.5/2(f). 5651 kapsamındaki trafik kayıtları yükümlülükleri saklıdır.</li>
</ul>

<h2>5) Alıcı Grupları ve Veri Aktarımı</h2>
<h3>5.1. Yurt içi alıcı grupları</h3>
<ul>
  <li>Barındırma, güvenlik, içerik dağıtım ağı (CDN), DDoS koruma ve altyapı tedarikçileri,</li>
  <li>E-posta/SMS/ileti altyapısı sağlayıcıları,</li>
  <li>Analitik, ölçüm, hata &amp; performans izleme tedarikçileri,</li>
  <li>Etiket/çerez yönetimi (CMP), A/B test, kişiselleştirme ve deneyim araçları,</li>
  <li>İş ortakları ve entegrasyon yapılan işletmeler (ilgili işletmenin sitesindeki PUANDEKS widget’ında gerekli ve ölçülü veriler),</li>
  <li>Hukuk, denetim, mali müşavirlik ve danışmanlık hizmeti sağlayıcılar,</li>
  <li>Yetkili kişi, kurum ve kuruluşlar (mevzuat gereği talep halinde).</li>
</ul>
<p><em>Hukuki sebep:</em> KVKK m.8 kapsamında; ilgili çerez kategorisinde m.5/2 (c, ç, e, f) hallerinde zorunluluk, diğer hallerde m.5/1 (açık rıza).</p>

<h3>5.2. Yurt dışına aktarım</h3>
<p>Bazı çerez/SDK ve altyapılar yurt dışında mukim sağlayıcılarca sunulabilir (ör. bulut barındırma, CDN, analitik, hata/perf. izleme, reklam/iletim). Bu hallerde aktarım:</p>
<ul>
  <li>KVKK m.9/3 ve ilgili Yönetmelik uyarınca <strong>standart sözleşme</strong> ile uygun güvenceler sağlanarak ve/veya</li>
  <li>İlgili çerez kategorisi <strong>açık rızanıza</strong> dayanıyorsa KVKK m.9/6 kapsamındaki <strong>arızi aktarım</strong> modeliyle</li>
</ul>
<p>gerçekleştirilir. Çerez tercih panelinde yurt dışı sağlayıcı bulunan çerezler ayrı ayrı gösterilir.</p>

<p><strong>Not:</strong> PUANDEKS <em>widget entegrasyonları</em> kapsamında, entegrasyonun çalıştırılması, güvenliği ve ölçümlenmesi amacıyla yalnızca 
gerekli ve ölçülü çerez/benzeri teknolojiler devreye alınır; yurt içi/yurt dışı aktarım gerektiren hallerde bu politikanın 
5.2 maddesindeki güvenceler uygulanır.</p>

<h2>6) Saklama ve İmha</h2>
<p>Her bir çerezin saklama süresi, o çerezin yaşam süresi ile sınırlıdır (panelde Süre sütunu). Onay/ret tercih kayıtları ve hukuki yükümlülüklere ilişkin loglar 
mevzuattaki zorunlu süreler boyunca; açık süre yoksa olası uyuşmazlıklar gözetilerek genel zamanaşımı sürelerini aşmamak kaydıyla saklanır. 
5651 kapsamındaki trafik kayıtları ilgili mevzuatta öngörülen asgari sürelerde tutulur. Süresi dolan veriler periyodik imha politikamız uyarınca silinir,
yok edilir veya anonimleştirilir. Açık rızanın geri alınması halinde, rızaya dayalı çerez verilerinin işlenmesi durdurulur; tarayıcı/cihazınızdaki 
çerezlerin kaldırılması için kullanıcı işlemi gerekebilir.</p>

<h2>7) Çerez Tercihleri ve Onay Yönetimi</h2>
<ul>
  <li>Zorunlu çerezler haricindeki kategoriler varsayılan olarak kapalıdır; yalnızca açık rızanızla etkinleşir.</li>
  <li>Rızanızı dilediğiniz an değiştirebilir veya geri alabilirsiniz (Platform altındaki “Çerez Tercihleri”).</li>
  <li>Tarayıcı/cihaz ayarlarından da çerezleri silebilir veya engelleyebilirsiniz. Zorunlu çerezlerin engellenmesi mümkün değildir; 
    engellenmesi halinde temel hizmetler çalışmayabilir.</li>
</ul>


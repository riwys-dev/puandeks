  <li><strong>Microsoft Edge:</strong> Ayarlar &gt; Çerezler ve site izinleri.</li>
  <li><strong>Opera:</strong> Ayarlar &gt; Gizlilik ve güvenlik &gt; Çerezler. (Yardım sayfalarından detaylara ulaşabilirsiniz.)</li>
  <li><strong>Mobil reklam kimliği:</strong> iOS: Ayarlar &gt; Gizlilik ve Güvenlik &gt; Takip. Android: Ayarlar &gt; Gizlilik &gt; Reklamlar.</li>
  <li><strong>Avrupa tercihler:</strong> İsteğe bağlı olarak YourOnlineChoices üzerinden kişiselleştirilmiş reklam tercihlerinizi yönetebilirsiniz.</li>
</ul>

<h2>9) Üçüncü Taraf Çerezler ve Sorumluluk</h2>
<p>Üçüncü taraf sağlayıcılar (analitik, reklam ağları, sosyal medya platformları vb.) kendi gizlilik ve çerez politikalarına tabidir. Bu tarafların 
yerleştirdiği çerezlerin yönetimi için ilgili tarafın politikaları geçerlidir. Şirket, üçüncü taraf sitelerin içerik ve gizlilik uygulamalarından sorumlu değildir.</p>

<h2>10) İlgili Kişi Haklarınız (KVKK m.11)</h2>
<ul>
  <li>Kişisel verilerinizin işlenip işlenmediğini öğrenme ve bilgi talep etme,</li>
  <li>İşleme amacını ve amaca uygun kullanılıp kullanılmadığını öğrenme,</li>
  <li>Yurt içinde/yurt dışında aktarıldığı üçüncü kişileri bilme,</li>
  <li>Eksik/yanlış işlenmişse düzeltilmesini isteme,</li>
  <li>KVKK m.7 kapsamındaki şartlar oluştuğunda silinmesini/yok edilmesini isteme,</li>
  <li>Düzeltme/silme/yok etme işlemlerinin aktarılan kişilere bildirilmesini isteme,</li>
  <li>Münhasıran otomatik sistemlerle analiz sonucu aleyhinize bir sonuca itiraz etme,</li>
  <li>Kanuna aykırı işleme nedeniyle zararın giderilmesini talep etme.</li>
</ul>

